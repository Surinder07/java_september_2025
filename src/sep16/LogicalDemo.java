package sep16;

public class LogicalDemo {
    public static void main(String[] args) {

        int x = 10;
        int y = 20;
        int a = 15;
        int b = 25;

        // give true of false
        System.out.println(x == y);  // false
        System.out.println(a <= y);   // true
        System.out.println(b >= a);  // true
        System.out.println(b != y);  // true
        System.out.println(a <= x); // false



    }
}
