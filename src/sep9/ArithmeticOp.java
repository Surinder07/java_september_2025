package sep9;

public class ArithmeticOp {
    public static void main(String[] args) {

        // ++
        int x = 10;
        x = x + 1;   // same as this   x++;  // increment op

        int y = 10;
        y = y + 1;  // y++;

        int z = 10;
        z = z - 1;   // same as this   z--;  decrement op




        System.out.println(x);
    }
}
